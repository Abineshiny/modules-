def m_to_km(meters):
  return meters / 1000

def km_to_m(kilometers):
  return kilometers * 1000

def gms_to_kgms(grams):
  return grams / 1000

def kgms_to_gms(kilograms):
  return kilograms * 1000

def c_to_f(celsius):
  return (celsius * 9/5) + 32

def f_to_c(fahrenheit):
  return (fahrenheit - 32) * 5/9
